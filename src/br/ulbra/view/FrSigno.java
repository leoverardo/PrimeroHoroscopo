    public String gerarCor() {
        String corAleatoria;
        String[] cor = {"Verde", "Azul", "Roxo", "Vermelho", "Amarelo", "Rosa", "Preto", "Branco", "Laranja", "Marrom", "Cinza", "Violeta"};
        Random randomCor = new Random();
        int vet = cor.length;
        int i = randomCor.nextInt(vet);
        String cores = cor[i];
        return cores;
    }
    public int calcularIdade(){
        int idade;
        int dia = Integer.parseInt(txtDia.getText()), mes = Integer.parseInt(txtMes.getText()), ano = Integer.parseInt(txtAno.getText());
         Calendar hoje = Calendar.getInstance();
        int diaA, mesA, anoA;
        diaA = hoje.get(Calendar.DATE);
        mesA = hoje.get(Calendar.MONTH)+1;
        anoA = hoje.get(Calendar.YEAR);
        idade=((anoA*365+mesA*12+diaA)-(ano*365+mes*12+dia))/365;
        
        return idade;
    }
