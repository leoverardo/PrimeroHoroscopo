    public int nomear(String nome) {
        int cont = 0;
        cont = (nome.length());
        if (cont >= 8) {
            nome = txtNome.getText();
        } else {
            JOptionPane.showMessageDialog(null, "Se esforça ai! Assim poderemos lhe oferecer melhores informações.");
        }
        return cont;
    }

    public String fazerTratamento() {
        String trato = "";
        if (rbMasc.isSelected()) {
            if (rbSolteiro.isSelected()) {
                trato = " ";
            } else {
                trato = "Sr.";
            }
        } else {
            if (rbSolteiro.isSelected()) {
                trato = "Srta.";
            } else {
                trato = "Sra.";
            }
        }
        return trato;
    }

